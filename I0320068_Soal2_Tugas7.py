# Fungsi floor
import math
nilai = 90
print ("Ahmed mendapatkan nilai fisika sebesar", math.floor(nilai), "pada ujian akhir semester 1")

# Fungsi min dan max
import math
Biologi = 90
Kalkulus = 97
Prokom = 75
print ("Nilai tertinggi yang Ahmed dapatkan pada semester ini ialah ", max(Biologi,Kalkulus,Prokom), ", yaitu pada matkul Kalkulus")
print ("Nilai terendah yang Ahmed dapatkan pada semester ini ialah ", min(Biologi,Kalkulus,Prokom), ", yaitu pada matkul Prokom")

# Fungsi sqrt
import math
Alas = 60
Tinggi = 60
S_miring = Alas ** 2 + Tinggi ** 2
